    <p>CodeIgniter is an Application Development Framework - a toolkit - for people who build web sites using PHP. Its goal is to enable you to develop projects much faster than you could if you were writing code from scratch, by providing a rich set of libraries for commonly needed tasks, as well as a simple interface and logical structure to access these libraries. CodeIgniter lets you creatively focus on your project by minimizing the amount of code needed for a given task.</p>

    <p>Where possible, CodeIgniter has been kept as flexible as possible, allowing you to work in the way you want, not being forced into working any certain way. The framework can have core parts easily extended or completely replaced to make the system work the way you need it to. In short, CodeIgniter is the malleable framework that tries to provide the tools you need while staying out of the way.</p>

    <h3>Who is CodeIgniter For?</h3>
    <p>CodeIgniter is right for you if:</p>

    <ul>
        <li>You want a framework with a small footprint.</li>
        <li>You need exceptional performance.</li>
        <li>You want a framework that requires nearly zero configuration.</li>
        <li>You want a framework that does not require you to use the command line.</li>
        <li>You want a framework that does not require you to adhere to restrictive coding rules.</li>
        <li>You are not interested in large-scale monolithic libraries like PEAR.</li>
        <li>You do not want to be forced to learn a templating language (although a template parser is optionally available if you desire one).</li>
        <li>You eschew complexity, favoring simple solutions.</li>
        <li>You need clear, thorough documentation.</li>
    </ul>

